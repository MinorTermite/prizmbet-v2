    # –°–æ—Ä—Ç–∏—Ä—É–µ–º: —Å–Ω–∞—á–∞–ª–∞ –ø–æ –¥–∞—Ç–µ, –ø–æ—Ç–æ–º –ø–æ –≤—Ä–µ–º–µ–Ω–∏
    def sort_key(m):
        months = {'—è–Ω–≤':'01','—Ñ–µ–≤':'02','–º–∞—Ä':'03','–∞–ø—Ä':'04','–º–∞–π':'05','–∏—é–Ω':'06',
                  '–∏—é–ª':'07','–∞–≤–≥':'08','—Å–µ–Ω':'09','–æ–∫—Ç':'10','–Ω–æ—è':'11','–¥–µ–∫':'12'}
        d = m.get('date','')
        t = m.get('time','')
        parts = d.split()
        if len(parts) == 2:
            day = parts[0].zfill(2)
            mon = months.get(parts[1], '01')
            return f"{mon}-{day} {t}"
        return f"99-99 {t}"
    matches.sort(key=sort_key)

    data = {
        "last_update": datetime.datetime.now(datetime.timezone.utc).strftime('%Y-%m-%d %H:%M:%S'),
        "matches": matches
    }
    tmp = OUTPUT_FILE + ".tmp"
    with open(tmp, 'w', encoding='utf-8') as f:
        json.dump(data, f, ensure_ascii=False, indent=2)
    if os.path.exists(OUTPUT_FILE):
        os.replace(tmp, OUTPUT_FILE)
    else:
        os.rename(tmp, OUTPUT_FILE)
    size_kb = os.path.getsize(OUTPUT_FILE) / 1024
    print(f"‚úì –°–æ—Ö—Ä–∞–Ω–µ–Ω–æ {OUTPUT_FILE} ({size_kb:.1f} KB)")

