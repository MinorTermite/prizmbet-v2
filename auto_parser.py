def make_match(sport, league, prefix, t1, t2, base_date, days_range, hours, odds_fn):
    days_offset = random.randint(0, days_range)
    h = random.choice(hours)
    m_time = random.randint(0, 59)
    dt = base_date + datetime.timedelta(days=days_offset, hours=h - base_date.hour, minutes=m_time - base_date.minute)
    dt = dt.replace(hour=h, minute=m_time, second=0, microsecond=0)
    p1, x, p2, p1x, p12, px2 = odds_fn()
    return {
        'sport': sport, 'league': league,
        'id': generate_id(prefix, t1, t2, dt),
        'date': format_date(dt), 'time': dt.strftime('%H:%M'),
        'team1': t1, 'team2': t2,
        'p1': p1, 'x': x, 'p2': p2,
        'p1x': p1x, 'p12': p12, 'px2': px2
    }

