def generate_id(prefix, t1, t2, dt):
    a = ''.join(c for c in t1.upper() if c.isalpha())[:3]
    b = ''.join(c for c in t2.upper() if c.isalpha())[:3]
    return f"{prefix}_{a}_{b}_{dt.strftime('%H%M')}"

